/**
  * @brief This function handles TIM2 global interrupt.
  */
void TIM2_IRQHandler(void)
{
  /* USER CODE BEGIN TIM2_IRQn 0 */
	if(LL_TIM_IsActiveFlag_UPDATE(TIM2)){
		//LL_GPIO_TogglePin(GPIOE,LL_GPIO_PIN_3);
		LL_TIM_ClearFlag_UPDATE(TIM2);
		LL_TIM_OC_SetCompareCH1(TIM2,motor[0]);		//PA0
		LL_TIM_OC_SetCompareCH2(TIM2,motor[2]);		//PA1
		LL_TIM_OC_SetCompareCH3(TIM2,motor[1]);		//PA2
		LL_TIM_OC_SetCompareCH1(TIM4,motor[3]);		//PD12
		LL_TIM_OC_SetCompareCH2(TIM4,motor[4]);		//PD13
		LL_TIM_OC_SetCompareCH3(TIM4,motor[5]);		//PD14
		condition_pid=1;

	}


  /* USER CODE END TIM2_IRQn 0 */
  /* USER CODE BEGIN TIM2_IRQn 1 */

  /* USER CODE END TIM2_IRQn 1 */
}

